package com.mycompany.imagej;

public class Geom {

    public double length, area, width, height, widthHeightRatio, bX, bY, comX, comY, yMid, xMid;

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getWidthHeightRatio() {
        return widthHeightRatio;
    }

    public void setWidthHeightRatio(double widthHeightRatio) {
        this.widthHeightRatio = widthHeightRatio;
    }

    public double getbX() {
        return bX;
    }

    public void setbX(double bX) {
        this.bX = bX;
    }

    public double getbY() {
        return bY;
    }

    public void setbY(double bY) {
        this.bY = bY;
    }

    public double getComX() {
        return comX;
    }

    public void setComX(double comX) {
        this.comX = comX;
    }

    public double getComY() {
        return comY;
    }

    public void setComY(double comY) {
        this.comY = comY;
    }

    public double getyMid() {
        return yMid;
    }

    public void setyMid(double yMid) {
        this.yMid = yMid;
    }

    public double getxMid() {
        return xMid;
    }

    public void setxMid(double xMid) {
        this.xMid = xMid;
    }

}
